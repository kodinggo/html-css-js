// Tipe Data Primitif

var angka = 1; // number

console.log(typeof angka);

var nama = "Budi"; // string

console.log(typeof nama);

var isCorrect = true; // boolean

console.log(typeof isCorrect);

var umur; // undefine

console.log(typeof umur);

// Tipe Data Object

// object
var data = {
    'nama': 'Bambang',
    'usia': 18,
    'alamat': null
};
console.log(data['nama']);
console.log(data.nama);

console.log(typeof data);

// array
var month = ["Januari", "Februari", "Maret", 10, 20];

// Mengakses element array dengan index
console.log(month[0]); // Januari
console.log(month[2]); // Maret

console.log(typeof month);

// javascript
var nama = null;

console.log(typeof nama);